	map[5] = "10000001000000000000000000000000000000000000011";
	map[6] = "1000010000000000000000000000000000000000000000001";
	map[7] = "10000000000000000000000000000000000000000000011";
	map[8] = "10000000000000000000000000000000000000000000011";
	map[9] = "11111111111111111111111111111111111111111111111";
