void	draw_map(t_game *parse, t_cimg *image)
{
	char	**map;
	int		y;
	int		x;

	map = parse->map;
	y = 0;
	while (map[y])
	{
		x = 0;
		while (map[y][x])
		{
			if (map[y][x] == '1')
				draw_square(x * BLOCK, y * BLOCK, BLOCK, image);
			x++;
		}
		y++;
	}
}
