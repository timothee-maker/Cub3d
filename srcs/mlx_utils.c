#include "cub3d.h"

void	clear_image(t_cimg *img)
{
	int	i;
	int	j;

	i = 0;
	while (i < WIDTH)
	{
		j = 0;
		while (j < HEIGHT)
		{
			put_pixel(i, j, 0, img);
			j++;
		}
		i++;
	}
}