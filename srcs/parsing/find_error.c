#include "cub3d.h"

int	empty_line(char **map, int j, int i)
{
	i = 0;
	while (map[j][i])
	{
		if (map[j][i] != ' ' && map[j][i] != '\n' && map[j][i] != '\t')
			return (0);
		i++;
	}
	return (1);
}
